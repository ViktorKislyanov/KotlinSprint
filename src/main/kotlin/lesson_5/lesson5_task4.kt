package lesson_5

const val REGISTERED_USER_NAME = "Zaphod"
const val REGISTERED_USER_PASSWORD = "PanGalactic"

fun main() {

    println(
        """
        Внимание, пассажир. Моя обязанность, как обычно, незавидная, 
        - приветствовать вас на борту корабля "Heart of Gold". [вздыхает...] 
        Что ж, введи, пожалуйста, свое имя пользователя, чтобы 
